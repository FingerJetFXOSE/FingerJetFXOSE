  template <class T, class pair_t, uint8 m>
  inline void add(T & a, pair_t (& pairs)[m]) {
    for (size_t i = 0; i < m; i++) {
      a.add(pairs[i]);
    }
  }
  template <class pair_t>
  inline uint8 count(pair_t * cur) {
    uint8 cnt = 0;
    for (; cur; cur = cur->next) {
      cnt++;
    }
    return cnt;
  }
