    template <class Ty_, size_t M, size_t N>
    inline void TransposeMatrix(Ty_ (&scores)[M][N], int m, int n) {
      for (int i = 0; i < m; i++) {
        for (int j = i+1; j < n; j++) {
          std::swap(scores[i][j], scores[j][i]);
        }
      }
    }

